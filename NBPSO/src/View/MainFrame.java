    private void tabel_percobaanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_percobaanMouseClicked
        try{
            int id = Integer.parseInt(this.tabel_percobaan.getValueAt(this.tabel_percobaan.getSelectedRow(), 0).toString());
            Percobaan percobaan_terpilih = null;
            for(Skenario skenario : this.daftar_skenario){
                for(Percobaan percobaan : skenario.getDaftar_percobaan()){
                    if(id == percobaan.getId_percobaan()){
                        percobaan_terpilih = percobaan;
                        this.drawLogPercobaan(skenario, percobaan_terpilih);
                        break;
                    }
                }
            }
        }
        catch(Exception io){
            // di luar tabel
        }
    }//GEN-LAST:event_tabel_percobaanMouseClicked

    private void mulaiKlasifikasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mulaiKlasifikasiActionPerformed
        Skenario skenario = this.getSkenarioBasedOnName(this.list_skenario.getSelectedValue());
        Percobaan percobaan_baru = null;
        if(!this.opsi_pso.isSelected()){
          percobaan_baru = this.main_controller.doNaiveBayesClassifier(skenario);
        }else{
           HashMap<String, String> konfigurasi = new HashMap<String, String>();
           konfigurasi.put("Stoping_criteria", this.label_stoping_criteria.getSelectedItem().toString());
           konfigurasi.put("Stoping_point",this.label_input_stoping_point.getText());
           konfigurasi.put("Populasi", this.label_input_populasi.getText());
           konfigurasi.put("c1", this.label_input_c1.getText());
           konfigurasi.put("c2", this.label_input_c2.getText());      
           percobaan_baru = this.main_controller.doPSONaiveBayesClassifier(skenario, konfigurasi);
        }
        skenario.tambahPercobaan(percobaan_baru);
        this.aktifkanSkenario(skenario.getNama());
    }//GEN-LAST:event_mulaiKlasifikasiActionPerformed

