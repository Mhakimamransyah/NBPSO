    private void hapusSkenarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusSkenarioActionPerformed
       int index_hapus = 0;
       int id_skenario = 0;
        for(int i=0;i<this.daftar_skenario.size();i++){
           if(this.daftar_skenario.get(i).getNama().equalsIgnoreCase(this.list_skenario.getSelectedValue())){
               index_hapus = i;
               id_skenario = this.daftar_skenario.get(i).getId_skenario();
               break;
           }
        }
        this.main_controller.hapusSkenario(id_skenario);
        this.daftar_skenario.remove(index_hapus);
        this.drawListSkenario();       
    }//GEN-LAST:event_hapusSkenarioActionPerformed

    private void hapusPercobaanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusPercobaanActionPerformed
        int id_percobaan = Integer.parseInt(this.tabel_percobaan.getValueAt(this.tabel_percobaan.getSelectedRow(), 0).toString());      
        for(Skenario skenario : this.daftar_skenario){
            if(skenario.getNama().equalsIgnoreCase(this.list_skenario.getSelectedValue())){
                skenario.hapusPercobaan(id_percobaan);
                break;
            }
        }
        this.main_controller.hapusPercobaan(id_percobaan);
        this.aktifkanSkenario(this.list_skenario.getSelectedValue());
    }//GEN-LAST:event_hapusPercobaanActionPerformed

