    private void setTabelData(){
         DefaultTableModel tabel = (DefaultTableModel) this.tabel_hasil_cross_validation.getModel();
         tabel.setRowCount(0);
         Object[] object;
         int index = 0;
         for(Double akurasi : this.hasil_percobaan.getAkurasi_nb()){
             object = new Object[3];
             object[0] = " "+(index+1);
             object[1] = this.hasil_percobaan.getAkurasi_nb().get(index);
             object[2] = this.hasil_percobaan.getAkurasi_nbpso().get(index);
             tabel.addRow(object);
             index++;
         }
         this.centeringTable();
         this.panel_log.removeAll();
         this.drawLogResult();
         this.drawRerataAkurasi();
    }
    
     private void centeringTable(){
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment( JLabel.CENTER );
       for(int i=0;i<this.tabel_hasil_cross_validation.getColumnCount();i++){
           this.tabel_hasil_cross_validation.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
       }    
    }
     
