    private void mulaiKlasifikasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mulaiKlasifikasiActionPerformed
        int kfold = Integer.parseInt(this.input_k_fold.getModel().getValue().toString());
        konfigurasi_pso = new HashMap<String, String>();
        if(kfold > 1){
            try{
            konfigurasi_pso.put("Generasi",this.label_input_generasi.getText());
            konfigurasi_pso.put("Populasi", this.label_input_populasi.getText());
            konfigurasi_pso.put("c1", this.label_input_c1.getText());
            konfigurasi_pso.put("c2", this.label_input_c2.getText());
            this.hasil_percobaan = this.main.doClassificationData(kfold, konfigurasi_pso);
            this.setTabelData();   
          }catch(Exception e){
              JOptionPane.showMessageDialog(null,"Parameter salah atau tidak lengkap","Ooops!",JOptionPane.ERROR_MESSAGE);     
          }    
        }
        else{
            JOptionPane.showMessageDialog(null,"K fold jangan 0 atau 1, dak tau cakmano nak baginyo","Deeh!!",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_mulaiKlasifikasiActionPerformed

